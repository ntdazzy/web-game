<div class="post-content">
<h2><strong><span style="color:#3498db">Thời gian hoạt động:</span></strong> <strong><span style="color:#c0392b">10:00 ngày 06.10.2025 – 23:59 ngày 14.10.2025</span></strong></h2>
<h2><strong><span style="color:#3498db">Những server tham gia Tổ đội liên đấu lần này:</span></strong> <span style="color:#000000">S1 ~ S30</span></h2>
<p>Hải tặc chiến đấu (Tổ Đội Chiến) chia thành 2 giai đoạn:<br/>
•    Giai đoạn tích điểm<br/>
•    Giai đoạn vòng trong. <br/>
Vòng tích điểm sẽ chọn ra Top 16. Đội vào vòng 16 có thể tham gia giai đoạn Vòng trong.</p>
<p style="text-align:center"><img alt="" src="../st-ms/files/uploads/files/Test/0210-1.png" style="height:280px; width:551px"/></p>
<h2><span style="color:#3498db"><strong>Giai đoạn báo danh: </strong></span>10:00 06/10/2025 đến 10:00 07/10/2025</h2>
<p style="text-align:justify">Cấp nhân vật đạt đến lv 70 có thể tham gia Hải tặc chiến đấu,  <span style="color:#e74c3c">người chơi cùng server có thể tổ đội</span>.3 người hợp thành 1 tổ đội.  </p>
<ul>
<li style="text-align:justify"><strong>Quy tắc chiến đấu:</strong></li>
</ul>
<p style="text-align:justify">Giai đoạn tích điểm mỗi một thành viên đưa 2 đồng đội không trùng nhau ra trận (nhân vật chính có thể trùng nhau) hợp thành tiểu đội chiến đấu,  thi đấu cùng với những đội khác; giai đoạn vòng trong 3 thành viên lần lượt dùng tiểu đội 5 người của mình để chiến đấu với tiểu đội chiến đấu của 3 thành viên đối phương.</p>
<ul>
<li style="text-align:justify"><strong>Thông tin chiến đấu:</strong></li>
</ul>
<p style="text-align:justify">Dữ liệu liên quan tới người chơi và chiến đấu là những dữ liệu liên quan ảnh hưởng đến độ mạnh yếu trong khi chiến đấu của người chơi như đồng đội,  pet,  trang bị,  trận hình,  bảo thạch,  tinh bàn…. </p>
<ul>
<li style="text-align:justify"><span style="color:#e74c3c"><strong>Cập nhật thông tin chiến đấu:</strong></span></li>
</ul>
<p style="text-align:justify">Để bảo đảm dữ liệu của Hệ thống hải tặc chiến đấu do server lưu là dữ liệu thông tin chiến đấu mới nhất của người chơi,  sau mỗi lần người chơi thay đổi thông tin chiến đấu,  cần nhấn vào “Cập nhật thông tin chiến đấu” để truyền thông tin chiến đấu mới nhất của người chơi lên server. Trong thời gian Hải tặc chiến đấu nếu như dữ liệu thông tin chiến đấu của người chơi có thay đổi,  mà người chơi không nhấn vào “Cập nhật thông tin chiến đấu”,  thì dữ liệu thông tin chiến đấu của người chơi trong Hệ thống“Hải tặc chiến đấu” sẽ không thay đổi,  có thể sẽ dẫn đến việc thay đổi thứ tự đánh trước đánh sau.</p>
<h2><span style="color:#3498db"><strong>Giai đoạn tích điểm: </strong></span>10:00 07/10/2025  đến 22:00 10/10/2025</h2>
<p>1.    3 thành viên mỗi 1 thành viên đưa 2 đồng đội ra trận hợp thành tiểu đội chiến đấu, đội trưởng sẽ ghép cặp đối thủ chiến đấu, và phát lệnh cặp đối thủ nào đánh trước.<br/>
2.    Sau khi ghép cặp, có thể xem thông tin đối thủ, cũng có thể ghép cặp lại, mỗi một đội lần lượt có 3 lần xem thông tin và ghép cặp lại.<br/>
3.    Người chơi có thể mua số lần xem thông tin và ghép cặp lại.<br/>
4.    Mỗi lần chiến đấu sẽ có phần thưởng Beri, Danh vọng.<br/>
5.    Hàng ngày có 25 lần thách đấu, 10:00 đến 22:00 hàng ngày có thể thách đấu.<br/>
6.    Đội thách đấu thắng lợi có thể nhận được 20 tích điểm, thất bại nhận được 3 tích điểm.<br/>
7.    Sau khi vòng tích điểm kết thúc căn cứ vào xếp hạng tích điểm phát thưởng, Top 16 tích điểm sẽ được vào vòng trong.</p>
<h2><span style="color:#3498db"><strong>Giai đoạn vòng trong:</strong></span></h2>
<p style="text-align:center"><img alt="" src="../st-ms/files/uploads/files/Test/0210-2.png" style="height:156px; width:276px"/></p>
<p style="text-align:justify">Top 16 được chọn ra ở vòng tích điểm chia thành 8 nhóm, mỗi nhóm 2 đội thi đấu, khoảng cách mỗi vòng là 1 ngày.<br/>
1.    Mỗi vòng thi đấu chia thành 5 ván, đội thắng 3 ván sẽ được vào vòng tiếp theo<br/>
2.    Mỗi ván thi đấu mỗi thành viên của 2 đội cử ra 5 đồng đội để chiến đấu với 5 đồng đội của đối phương,  bất luận thắng thua, vòng đấu tiếp theo đều thay đồng đội khác ra trận.<br/>
3.    Thành viên có lực chiến cao đánh trước, đội nào thắng đối phương 2 lần là đội thắng cuộc.<br/>
4.    Mỗi ván chiến đấu cách nhau 30 phút, trước khi mỗi ván thi đấu bắt đầu có thể điều chỉnh thứ tự ra trận của đội đó.<br/>
5.    Tất cả người chơi ở server của Quán quân Hải tặc chiến đấu sẽ nhận được quà toàn server.</p>
<ul>
<li style="text-align:justify"><strong>Trợ uy </strong></li>
</ul>
<p style="text-align:justify">Giai đoạn vòng trong, mỗi vòng người chơi chỉ được chọn 1 tiểu đội để trợ uy, 10 phút trước mỗi vòng đấu không thể  trợ uy. Đội được trợ uy nếu như vào vòng tiếp theo, thì người chơi trợ uy có thể nhận được, 2500 Beri *lv nhân vật, 150 Danh vọng.  </p>
<ul>
<li style="text-align:justify"><strong>Tăng tiến tình cảm </strong></li>
</ul>
<p style="text-align:justify">Sau mỗi mùa Hải tặc chiến đấu kết thúc,  người chơi có thể tăng tiến tình cảm với đội quán quân,  nhấn vào “Xem quán quân”→“Tăng tiến tình cảm”,  tiêu Beri hoặc vàng là có thể nhận được phần thưởng “Tăng tiến tình cảm”,  mỗi mùa Hải tặc chiến đấu chỉ được tăng tiến tình cảm 1 lần với quán quân. Thành viên thuộc đội quán quân không được tăng tiến tình cảm.</p>
