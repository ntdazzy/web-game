<div class="post-content">
<p><strong>Tên HĐ: <span style="color:#c0392b">Công hội chiến liên sever</span></strong><br/>
<strong>Phạm vi tham gia:</strong> từ Server 1 tới Server 26</p>
<p>Đua top tranh bá tất cả chia thành 2 giai đoạn: <span style="color:#c0392b">Giai đoạn vòng loại và giai đoạn Liên Đấu</span>. Vòng loại sẽ chọn ra Top của nhóm này 16, công hội vào TOP 16 có thể tham gia vòng Liên Đấu.</p>
<p style="text-align:center"><img alt="" src="../st-ms/files/uploads/files/Test/0409-2.png" style="height:157px; width:609px"/></p>
<h2><strong><span style="color:#3498db">Giai Đoạn Báo Danh</span></strong></h2>
<p style="text-align:center"><img alt="" src="../st-ms/files/uploads/files/Test/0409-3.png" style="height:27px; width:609px"/></p>
<ul>
<li style="text-align:justify">Công hội đạt lv30, và Xếp hạng trận doanh của C.hội ở Top 5, và các C.hội có số thành viên là 15 thì có thể tham gia Đua top tranh bá.  Ấn nút “Đăng kí” của HT Đua top tranh bá, hội trưởng hoặc hội phó của công hội thỏa mãn điều kiện đăng kí có thể đăng kí. Sau khi đăng kí cần đợi 5 phút mới có thể xem thành viên ra trận mặc định và cập nhật thông tin chiến đấu.</li>
<li style="text-align:justify">Mặc định thành viên ra trận:  Sau khi đăng kí sẽ mặc định chọn thành viên trong Top 40 về độ cống hiến C.hội làm thành viên ra trận. Thành viên trong top 15 chiến lực và hội trưởng, không thể rời trận, hội trưởng có thể cài đặt thành viên ra trận còn lại. </li>
<li style="text-align:justify">Thông tin chiến đấu: Dữ liệu liên quan đến người chơi và chiến đấu, tức là tất cả những thông tin có thể ảnh hưởng đến độ mạnh yếu của người chơi như đồng đội, pet, trang bị, trận hình, bảo thạch, tinh bàn… </li>
<li style="text-align:justify"><span style="color:#c0392b">Cập nhật thông tin chiến đấu：</span>Để đảm bảo những gì mà HT Đua top tranh bá của server lưu giữ là dữ liệu thông tin chiến đấu mới nhất của người chơi, sau mỗi lần người chơi thay đổi thông tin chiến đấu, cần ấn vào “Cập nhật thông tin chiến đấu” trong “Thông tin chiến đấu” để truyền thông tin chiến đấu mới nhất của người chơi tới server game. Trong thời gian Đua top tranh bá nếu như dữ liệu thông tin chiến đấu của người chơi có thay đổi, mà người chơi không nhấn vào “Cập nhật thông tin chiến đấu”, thì dữ liệu thông tin chiến đấu của người chơi được lưu trong HT “Đua top tranh bá” sẽ không thay đổi, có khả năng sẽ dẫn đến sự thay đổi thứ tự đánh trước đánh sau trong chiến đấu. </li>
</ul>
<h2><strong><span style="color:#3498db">Giai Đoạn Vòng Loại</span></strong></h2>
<p style="text-align:center"><img alt="" src="../st-ms/files/uploads/files/Test/0409-4.png" style="height:27px; width:609px"/></p>
<ol>
<li style="text-align:justify">Phân chia ngẫu nhiên đối thủ, mỗi trận 1 ván. Mỗi ván cách nhau 20 phút. </li>
<li style="text-align:justify">Thành viên tham gia của C.hội sẽ căn cứ vào lực chiến đấu từ thấp tới cao để xuất trận, trận thứ 1 do người chiến lực cao đánh trước, còn lại sẽ luân phiên thay đổi thứ tự xuất trận</li>
<li style="text-align:justify">Sau khi hòa, thất bại hoặc đạt đến số lần thắng lớn nhất rời trận, người thắng kế thừa lượng máu tiếp tục trận tiếp theo, cho đến khi toàn bộ thành viên của 1phe đều rời trận thì mới kết thúc.</li>
<li style="text-align:justify">Công hội sẽ không gặp lại đối thủ đã bị loại</li>
<li style="text-align:justify">Nếu như công hội thất bại 3 trận, thì sẽ bị loại. </li>
<li style="text-align:justify">Sau khi chọn ra Top 16, sẽ dừng vòng loại. </li>
<li style="text-align:justify">Người chơi có thể xem thành tích của C.hội mình.</li>
</ol>
<h2><strong><span style="color:#3498db">Giai Đoạn Liên Đấu</span></strong></h2>
<p style="text-align:center"><img alt="" src="../st-ms/files/uploads/files/Test/0409-7.png" style="height:105px; width:609px"/></p>
<ol>
<li style="text-align:justify">Top 16 được chọn từ vòng loại sẽ phân thành 8 nhóm, mỗi nhóm 2 công hội thi đấu, mỗi vòng cách nhau 1 ngày.</li>
<li style="text-align:justify">Mỗi trận thi đấu, sẽ chia thành 5 nhóm nhỏ, mỗi nhóm 8 người, 2 bên căn cứ vào lực chiến đấu từ thấp tới cao để xuất trận. </li>
<li style="text-align:justify">Khoảng cách mỗi nhóm chiến đấu là 30 phút, người thắng trong mỗi trận đấu sẽ kế thừa lượng máu sau khi chiến đấu, tiếp tục trận sau. </li>
<li style="text-align:justify">Sau khi hòa, chiến đấu thất bại hoặc lần thắng liên tiếp đạt mức lớn nhất thì rời trận, cho đến khi toàn bộ thành viên của 1 phe đều rời trận thì mới kết thúc.</li>
<li style="text-align:justify">Tất cả người chơi trong server của Quán quân Đua top tranh bá đều nhận được lễ bao: 25000 beri*lv, 500 danh vọng.</li>
</ol>
<p style="text-align:justify"><span style="color:#c0392b">► Lần thắng:</span> Mỗi vòng thi đấu (Cả Vòng thi đấu 8 chọn 4 được coi là 1 vòng) lần thắng lớn nhất của người chơi là 3 lần, sau khi mỗi người chơi đánh hòa, bị người chơi khác đánh bại hoặc đạt lần thắng lớn nhất thì sẽ rời trận.<br/>
<span style="color:#c0392b">► Tăng thêm lần thắng lớn nhất: </span>Số lần thắng lớn nhất trong vòng loại cố định là 3 không thể tăng thêm. Trong vòng trong, có thể tăng thêm số lần thắng lớn nhất, từ 3 lần lên 5 lần, sau khi tăng lên chỉ có tác dụng trong vòng đấu đó.</p>
<h3><span style="color:#9b59b6"><strong>Trợ Uy:</strong></span></h3>
<p>Giai đoạn vòng trong, mỗi trận người chơi có thể chọn 1 Công hội để trợ uy, 10 phút trước mỗi trận thi đấu không thể trợ uy. Công hội được trợ uy nếu như vào vòng tiếp theo, thì người chơi trợ uy nhận được  2500 Beri*lv nhân vật, 300 danh vọng.</p>
<h3><span style="color:#9b59b6"><strong>Tăng Tiến Tình Cảm:</strong></span></h3>
<p>Sau mỗi mùa Đua top tranh bá kết thúc, người chơi có thể tăng tiến tình cảm với quán quân, ấn vào “Xem quán quân”→“Tăng tiến tình cảm”, dùng beri hoặc vàng có thể nhận được p.thưởng “Tăng tiến tình cảm”, mỗi mùa Đua top tranh bá chỉ được 1 lần tăng tiến tình cảm với quán quân.</p>
