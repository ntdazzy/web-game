<h2><strong><span style="color:#3498db">1. Điều kiện</span></strong></h2>
<ul>
<li>
<div>Nhân vật đạt cấp độ <span style="color:#e74c3c">Lv 70</span> có thể tham gia HĐ</div>
</li>
<li>
<div>Những server tham gia Vương Giả Chiến: <span style="color:#e74c3c">S1-S23</span></div>
</li>
</ul>
<div><strong>Thời gian bắt đầu từ: 11:30 ngày 20-08-2025 – 23:59 ngày 02-09-2025</strong></div>
<h2><strong><span style="color:#3498db">2. Phạm vi HĐ</span></strong></h2>
<h3><span style="color:#9b59b6"><strong>Nội dung HĐ</strong></span></h3>
<h4><strong>     Quy tắc HĐ</strong></h4>
<p>-    Vương giả chiến liên server chia thành 2 giai đoạn：Vương giả chiến từng server và Vương giả chiến liên server. Mỗi 1 giai đoạn đều cần phải trải qua vòng loại và vòng trong, chọn ra Top 32</p>
<p style="text-align:center"><img src="/st-ms/files/uploads/files/image-20250819173814-1.png" style="height:503px; width:411px"/></p>
<h4 style="text-align:justify"><strong>     Vương giả chiến từng server:</strong></h4>
<ul>
<li><strong>Giai đoạn đăng ký: <span style="color:#e74c3c">Diễn ra từ 11:30 ngày 20-08-2025 đến 12:00 ngày 21-08-2025</span></strong></li>
</ul>
<p style="text-align:justify">-    Người chơi LV 70 trở lên, nhấn vào nút “Báo danh” của hệ thống Cuộc chiến Vương giả, thỏa mãn điều kiện báo danh thì có thể báo danh, sau khi báo danh người chơi nếu như muốn update thông tin chiến đấu của bản thân cần phải nhấn vào nút “Update thông tin chiến đấu”.<br/>
-    Thông tin chiến đấu: Người chơi và các dữ liệu chiến đấu liên quan, tức là tất cả các thông tin liên quan có thể ảnh hưởng đến độ mạnh yếu khi chiến đấu của người chơi như Đồng đội, Pet, trang bị, trận hình, bảo thạch, tinh bàn.<br/>
-    Cập nhật thông tin chiến đấu: Để đảm bảo những dữ liệu mà hệ thống Cuộc chiến vương giả trong server lưu giữ là dữ liệu thông tin chiến đấu mà người chơi muốn lưu lại nhất, sau mỗi lần người chơi thay đổi thông tin chiến đấu, cần nhấn vào “Cập nhật thông tin chiến đấu” để truyền thông tin chiến đấu mới nhất của người chơi lên server. Trong thời gian Cuộc chiến Vương giả nếu như dữ liệu thông tin người chơi có thay đổi, mà người chơi chưa nhấn vào “Cập nhật thông tin chiến đấu”, thì dữ liệu thông tin chiến đấu của người chơi được lưu giữ trong hệ thống “Cuộc chiến Vương giả “sẽ không thay đổi.</p>
<ul>
<li style="text-align:justify"><strong>Giai đoạn vòng loại: <span style="color:#e74c3c">Diễn ra từ 23-08-2025 đến 27-08-2025</span></strong></li>
</ul>
<p style="text-align:justify">thủ chiến đấu, mỗi vòng chiến đấu ghép cặp 1 đối thủ, thời gian giãn cách mỗi vòng là 10 phút, người đánh trước vẫn phải căn cứ vào chiến lực của người chơi, người đánh trước có lực chiến đấu cao, thất bại 3 ván sẽ thị đào thải. Khi cuối cùng còn lại 32 người, thì nhóm thắng cuộc của Vòng loại, 32 người chơi nhóm thắng cuộc gọi là TOP 32 Tân Thế Giới.<br/>
-    Sau khi Vòng loại kết thúc, tiến hành Vòng loại giữa nhóm thua cuộc, người chơi nhóm thua cuộc là để loại đi những người chơi báo danh khác sau 32 người chơi nhóm thắng cuộc, cơ chế thi đấu như trên, khi cuối cùng còn lại 32 người, thì vòng loại nhóm thua cuộc kết thúc, 32 người chơi nhóm thua cuộc gọi là TOP 32 Đại Hải Trình.</p>
<ul>
<li style="text-align:justify"><strong>Giai đoạn vòng trong:</strong></li>
</ul>
<p style="text-align:center"><img src="/st-ms/files/uploads/files/image-20250819173917-2.png" style="height:115px; width:315px"/></p>
<p style="text-align:justify">-    Quy tắc vòng trong tương tự với hệ thống Lôi Đài, khác ở chỗ là sử dụng hình thức thi đấu 5 ván thắng 3, thời gian giãn cách mỗi ván là 6 phút, mỗi vòng 09 phút, Tân Thế Giới và Đại Hải Trình tiến hành song song; người chơi mỗi vòng có thể chọn 1 tuyển thủ yêu thích để trợ uy, nếu như người chơi được trợ uy tiến vào vòng tiếp theo thì người chơi sẽ được phần thưởng.</p>
<h4 style="text-align:justify"><strong>     Vương giả chiến liên server:</strong></h4>
<ul>
<li style="text-align:justify"><strong>Giai đoạn đăng ký:</strong></li>
</ul>
<p style="text-align:justify">-    Cuộc chiến Vương giải liên server không cần báo danh, Top 32 Tân Thế Giới và Đại Hải Trình của Cuộc chiến vương giải mỗi server sẽ tự động báo danh vào vòng Cuộc chiến Vương giả Liên server</p>
<p style="text-align:center"><img src="/st-ms/files/uploads/files/image-20250819174145-4.png" style="height:33px; width:357px"/></p>
<ul>
<li><strong>Giai đoạn vòng loại:  <span style="color:#e74c3c">15:00 28-08-2025 – 11:09 30-08-2025</span></strong></li>
</ul>
<p><span style="color:#000000">-    Cơ chế thi đấu giống với giai đoạn Vòng loại của Cuộc chiến Vương giả từng server, tức là những người báo danh sẽ thi đấu để chọn ra Top 32 Tân Thế Giới và Đại Hải Trình.</span></p>
<ul>
<li><strong>Giai đoạn vòng trong:</strong></li>
</ul>
<p style="text-align:center"><img src="/st-ms/files/uploads/files/image-20250819174248-5.png" style="height:133px; width:392px"/>​​​​​</p>
<p>-    Cơ chế thi đấu tương tự giai đoạn Vòng trong của từng server, tức là chọn ra Quán quân từ Top 32 Tân Thế Giới và Đại Hải Trình.</p>
<h3><span style="color:#9b59b6"><strong>Tăng tiến tình cảm:</strong></span></h3>
<p><span style="color:#000000">-    Sau khi Cuộc chiến vương giả liên server kết thúc, người chơi có thể tăng tiến tình cảm với Quán quân Tân Thế Giới của Cuộc chiến liên server, nhấn vào “Xem quán quân”→“Tăng tiến tình cảm”, tiêu 1 số beri hoặc vàng nhất định là có thể nhận được phần thưởng “Tăng tiến tình cảm”, mỗi mùa Liên đấu server chỉ có thể tiến hành 1 lần tăng tiến tình cảm với Quán quân.</span></p>
