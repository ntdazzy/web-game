</div>
<div class="post-content">
<h1><strong>I. Điều kiện:</strong></h1>
<ul>
<li><strong>Đẳng cấp :</strong> Người chơi cấp độ 60 trở lên có thẻ báo danh hoạt động Cây ác ma.</li>
<li><strong>Thời gian diễn ra :</strong> <span style="color:#c0392b">10:00 ngày 16.09.2025 – 23:59 ngày 22.09.2025</span></li>
<li><strong>Phạm vi diễn ra :</strong> S1 đến S27.</li>
</ul>
<h1><strong>II. Nội dung</strong></h1>
<h2><strong><span style="color:#3498db">Quy trình</span></strong></h2>
<ul>
<li>Để vào giao diện hoạt động  , người chơi chọn vào biểu tượng “ Cây Ác Ma “ trong tính năng hoạt động.</li>
</ul>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Test/1209-1.png" style="height:460px; width:624px"/></p>
<ul>
<li>Độ trưởng thành của cây ác ma là tổng cống hiến của tất cả người chơi ,độ trưởng thành càng nhiều thì đẳng cấp của cây ác ma càng cao , phần thưởng cuối cùng càng lớn.</li>
<li>Có 3 cách để quyên góp cho cây ác ma :</li>
</ul>
<p style="margin-left:40px">►<strong> Quyên góp vật phẩm : </strong>Người chơi có thể quyên góp vật phẩm ở trong ba lô của mình để nhận được điểm cống hiến. （trang bị, cá giống, loa lớn, khoáng thạch…）</p>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Test/1209-2.png" style="height:305px; width:502px"/></p>
<p style="margin-left:40px"><strong>► Tưới nước :</strong> Quyên góp bằng beri.</p>
<p style="text-align:center"><img src="/st-ms/files/uploads/files/image-20250916085443-3.png" style="height:309px; width:382px"/></p>
<p style="margin-left:40px"><strong>► Bón phân :</strong> Quyên góp bằng vàng .</p>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Test/1209-4.png" style="height:311px; width:466px"/></p>
<h2><strong><span style="color:#3498db">Bảng xếp hạng cống hiến</span></strong></h2>
<p>Trên bảng xếp hạng cống hiến thể hiện các điểm cần lưu ý sau :</p>
<ul>
<li>Tổng cống hiến tích lũy : Đây là tổng điểm cống hiến của cá nhân người chơi tích lũy qua nhiều vòng hoạt động. Điểm tích lũy này sẽ không bị xóa sau mỗi vòng.</li>
<li>Điểm cống hiến vòng này : Điểm cống hiến của người chơi và công hội trong vòng hoạt động hiện tại.</li>
<li>Điểm cống hiến vòng này càng nhiều thì xếp hạng càng  cao, qua vòng này sẽ xóa đi.</li>
</ul>
<p style="margin-left:40px"><strong>► </strong>Xếp hạng cá nhân: căn cứ độ cống hiến của mỗi cá nhân vòng này để tiến hành xếp hạng. <br/>
<strong>► </strong>Xếp hạng Công hội: Căn cứ vào tổng giá trị độ cống hiến hiện tại của Top 30 người chơi của các Công hội để tiến hành xếp hạng, khi phát thưởng cuối cùng thì toàn bộ người chơi trong công hội đều có thể nhận được quà công hội. </p>
<ul>
<li>Điểm cống hiến của người chơi có thời gian gia nhập Công hội ít hơn 7 ngày sẽ không được tính vào trong xếp hạng Điểm cống hiến công hội. </li>
</ul>
<p><strong><span style="color:#c0392b">⇒ Chú ý:</span></strong></p>
<ul>
<li>Sau 22:00 hàng ngày không thể quyên góp, ngày cuối cùng của hoạt động cũng không thể quyên góp. </li>
<li>Cấp của Cây ác ma cứ 15 phút sẽ làm mới 1 lần, có thể thấy cấp mới nhất của cây. Bảng xếp hạng công hội cứ 30 phút sẽ làm mới 1 lần.</li>
<li>Người chơi có thể dùng điểm cống hiến để đổi vật phẩm trong Shop Ác Ma.</li>
</ul>
