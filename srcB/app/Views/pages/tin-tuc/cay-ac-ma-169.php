<ul>
<li>Chi tiết phần thưởng có thể nhấn vào  ở góc trên bên phải của Cây Ác Ma để vào xem. </li>
</ul>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Test/1209-7.png" style="height:55px; width:72px"/></p>
