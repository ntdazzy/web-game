<p><strong><span style="color:#e74c3c">Lưu ý: </span></strong>Nếu đã cài đặt trước đó, hệ thống sẽ thông báo trùng ứng dụng, chọn “Replace” để cập nhật ứng dụng mới.</p>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Content/CAIDAT1.png" style="height:135px; width:424px"/></p>
<h1>HƯỚNG DẪN FIX LỖI CƠ BẢN</h1>
<p>Nếu không thể mở ứng dụng, bạn làm như sau:</p>
<p><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><strong><span style="font-size:13.5pt"><span style='font-family:"Times New Roman",serif'>Bước 1: Tắt Gatekeeper để cho phép chạy ứng dụng ngoài App Store</span></span></strong></span></span></p>
<p><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>1. Mở <strong>Terminal</strong> (bạn có thể tìm trong Spotlight hoặc Launchpad).</span></span></span></span></p>
<p><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>2. Nhập lệnh sau và nhấn Enter<span style="color:red">:</span></span></span> <span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'><span style="color:red"> <strong>sudo spctl --master-disable</strong></span></span></span></span></span></p>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Content/caimacos1.png.jpg" style="height:247px; width:682px"/></p>
<p><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>4. Nhập <strong>mật khẩu máy Mac</strong> (khi nhập sẽ không hiện ra ký tự, cứ gõ xong rồi nhấn Enter).</span></span></span></span></p>
<p><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>5.Vào <strong>System Settings (Cài đặt Hệ thống)</strong> → <strong>Privacy &amp; Security (Riêng tư &amp; Bảo mật)</strong>.</span></span></span></span></p>
<p><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>6. Kiểm tra xem tùy chọn "Allow apps downloaded from: Anywhere" (Cho phép ứng dụng từ bất kỳ đâu) đã được bật.</span></span></span></span></p>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Content/caimacos2.png.jpg" style="height:591px; width:660px"/></p>
<p><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><strong><span style="font-size:13.5pt"><span style='font-family:"Times New Roman",serif'>Bước 2: Cấp quyền truy cập đầy đủ cho ứng dụng</span></span></strong></span></span></p>
<ul>
<li><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>Trong Terminal, tiếp tục nhập lệnh sau:  </span></span><strong><span style="font-size:10.0pt"><span style='font-family:"Times New Roman",serif'><span style="color:red">sudo xattr -cr /Applications/Hải\ Tặc\ Mạnh\ Nhất.app</span></span></span></strong><em><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>(Lưu ý: Đảm bảo tên file chính xác, bạn có thể đổi tên ứng dụng thành không dấu để dễ nhập hơn nếu cần)</span></span></em></span></span></li>
<li><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:12.0pt"><span style='font-family:"Times New Roman",serif'>Nhập lại mật khẩu nếu được yêu cầu.</span></span></span></span></li>
</ul>
<p style="text-align:center"><img alt="" src="/st-ms/files/uploads/files/Content/caimacos3.png.jpg" style="height:292px; width:720px"/></p>
<h3><span style="color:#000000"><span style="font-size:13.5pt"><span style='font-family:"Times New Roman",serif'><strong>Bước 3: Nếu gặp lỗi mở ứng dụng trên macOS 15 trở lên</strong></span></span></span></h3>
<h3><span style="color:#000000"><span style="font-size:13.5pt"><span style='font-family:"Times New Roman",serif'><strong>Một số phiên bản macOS mới có thể hiển thị lỗi khi mở ứng dụng. Nếu gặp lỗi:</strong></span></span></span></h3>
<ul>
<li><span style="font-size:12pt"><span style='font-family:"Times New Roman",serif'>Vào <strong>System Settings</strong> → <strong>Privacy &amp; Security</strong>.</span></span></li>
<li><span style="font-size:12pt"><span style='font-family:"Times New Roman",serif'>Tìm mục “<strong>Hải Tặc Mạnh Nhất” was blocked to protect your Mac</strong></span></span></li>
<li><span style="font-size:12pt"><span style='font-family:"Times New Roman",serif'>Bấm vào mục Open Anyway</span></span></li>
</ul>
<p style="text-align:center"><span style="font-size:12pt"><span style='font-family:"Times New Roman",serif'><img alt="" src="/st-ms/files/uploads/files/Content/htmn(1).jpg" style="height:481px; width:539px"/></span></span></p>
<p> </p>
<h3><span style="color:#000000"><span style="font-size:13.5pt"><span style='font-family:"Times New Roman",serif'><strong>Bước 4: Mở game và thưởng thức!</strong></span></span></span></h3>
<ul>
<li><span style="font-size:12pt"><span style='font-family:"Times New Roman",serif'>Quay lại thư mục <code><span style="font-size:10.0pt">Applications</span></code>, tìm biểu tượng <strong>Hải Tặc Mạnh Nhất</strong> và mở game.</span></span></li>
<li><span style="font-size:12pt"><span style='font-family:"Times New Roman",serif'>Nếu vẫn gặp lỗi, hãy thử khởi động lại máy và mở lại.</span></span></li>
</ul>
<p> </p>
<p><span style="font-size:16px"><strong><span style='font-family:"Times New Roman",serif'>Hướng Dẫn Mở Nhiều Tài Khoản</span></strong></span></p>
<ul>
<li><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:10.0pt"><span style='font-family:"Arial",sans-serif'><span style="color:black">Mở terminal gõ vào lệnh sau:</span></span></span></span></span></li>
<li><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><em><span style="font-size:10.0pt"><span style='font-family:"Arial",sans-serif'><span style="color:#bf8f00">open -n "/Applications/Hải Tặc Mạnh Nhất.app"</span></span></span></em></span></span></li>
<li><span style="font-size:11pt"><span style="font-family:Calibri,sans-serif"><span style="font-size:10.0pt"><span style='font-family:"Arial",sans-serif'><span style="color:black">Cứ 1 lần gõ lệnh là sẽ mở thêm 1 màn hình game mới để đăng nhập</span></span></span></span></span></li>
</ul>
<p><span style="color:#3498db">Chúc các Thuyền Trưởng cài đặt Client thành công và tận hưởng những trải nghiệm đặc sắc nhất của thế giới Hải Tặc Mạnh Nhất.</span></p>
