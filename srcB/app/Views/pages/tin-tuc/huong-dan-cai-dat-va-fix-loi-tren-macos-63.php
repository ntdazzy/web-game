<h1>CÀI ĐẶT CLIENT</h1>
<ul>
<li>Sau khi tải xuống, tìm file vừa tải ”HaiTacManhNhat.dmg”</li>
<li>Nhấp đúp vào file để bắt đầu quá trình cài đặt.</li>
<li>Kéo biểu tượng client vào thư mục Applications (hoặc thư mục bạn chọn)</li>
